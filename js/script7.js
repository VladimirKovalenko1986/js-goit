// Example 7 - Оператори порівняння та приведення типів
// Яким буде результат виразів?

console.log(5 > 4);

console.log(10 >= '7');

console.log('2' > '12'); //true то му що строкі порівнюються відповидно unicode
console.log('A'.charCodeAt()); //Код який показує положення символів відносно unicode
console.log('2' < '12');

console.log('4' == 4);

console.log('6' === 6);

console.log('false' === false);

console.log(1 == true);

console.log(1 === true);

console.log('0' == false);

console.log('0' === false);

console.log('Papaya' < 'papaya'); //true то му що p бильше значення ніє P

console.log('Papaya' === 'papaya');

console.log(undefined == null);

console.log(undefined === null);
