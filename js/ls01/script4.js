// Example 4 - Клас Math
// Напиши скрипт, який виводить у консоль заокруглені вгору / вниз і т.д.значення
// змінної value.Використовуй методи Math.floor(), Math.ceil() та Math.round().Перевір
// що буде в консолі при значеннях 27.3 та 27.9.

const value = 27.4;

console.log(Math.floor(value)); //просто відкідує значення після коми
console.log(Math.ceil(value)); //округлює в сторону в яку є чісло після коми
console.log(Math.round(value)); //як в математиці
